import socket

class NetworkManager:
    def get_ip_address(self):
        
        try:
            s = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
            s.connect(("8.8.8.8", 80))
            ip_address = s.getsockname()[0]
            s.close()
            return ip_address
        except Exception as e:
            return f"Error retrieving IP address: {e}"
